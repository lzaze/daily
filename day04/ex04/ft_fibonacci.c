int		ft_fibonacci(int index)
{
	int		a;
	int		b;
	int		c;

	a = 0;
	b = 1;

	while (index >= 0)
	{
		c = a + b;
		a = b;
		b = c;
		index--;
	}

	return (c);
}

int main()
{
	printf("%d", ft_fibonacci(10));
}
