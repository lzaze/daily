char	*ft_strrev(char *str)
{
	char	t;
	int		j;
	int		i;

	i = 0;
	j = 0;

	while (str[i] != '\0')
	{
		i++;
	}

	i--;

	while (i >= j)
	{
		t = str[j];
		str[j] = str[i];
		str[i] = t;
		j++;
		i--;
	}

	return (str);
}
