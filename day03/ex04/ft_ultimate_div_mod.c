void	ft_ultimate_div_mod(int *a, int *b)
{
	if (*b > 0)
	{
		int		temp_a;
		int		temp_b;
	
		temp_a = *a;
		temp_b = *b;
		*a = temp_a / temp_b;
		*b = temp_a % temp_b;
	}
}
